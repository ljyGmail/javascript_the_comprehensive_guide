// 3.3.2 Operators for Easier Assignment
"use strict"

let result = 5;
result += 11;
console.log(`result: ${result}`);
result -= 1;
console.log(`result: ${result}`);
result /= 3;
console.log(`result: ${result}`);
result *= 4;
console.log(`result: ${result}`);
result %= 5;
console.log(`result: ${result}`);
