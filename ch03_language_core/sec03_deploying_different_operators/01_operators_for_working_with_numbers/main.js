// 3.3.1 Operators for Working with Numbers
"use strict"

console.log("20 + 20 = " + (20 + 20));
console.log("20 - 15 = " + (20 - 15));
console.log("20 * 5 = " + (20 * 5));
console.log("100 / 5 = " + (100 / 5));
console.log("20 % 6 = " + (20 % 6));
let x = 5;

console.log("x++: " + x++);
console.log("x = " + x)
console.log("x--: " + x--);
console.log("x = " + x)

console.log("5 ** 5 = " + (5 ** 5));
